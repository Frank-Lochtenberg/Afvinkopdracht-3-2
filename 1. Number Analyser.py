print("Hello user!")                                   # Hello

i = int(input("Can I get a integer from you?"))        # Asks for integer and prints it
print(("This is your integer:") + str(i))

if i <= 0:                                             # Checks if the integer is smaller, the same or greater than 0
   print("Your integer is Negative!")                  # Then prints the result
if i == 0:
   print("Your integer is Zero!")
if i >= 0:
   print("Your integer is Positive!")
if (i % 2) == 0:                                       # Checks if the integer is even or odd
   print("Your integer is Even!")                      # Then prints the result
if (i % 2) != 0:
   print("Your integer is Odd!")

print("Goodbye user!")                                 # Goodbye


        
        
   
